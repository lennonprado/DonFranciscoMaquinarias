				<h3 class="title" style="">Carina y Los Fierros del Campo</h3>
        		<div style="width:260px; float:left; margin-right:10px">
                <a href="files/IMG_2971-1024x768.jpg" class="image-zoom home-thumb-zoom">
							<img width="260"src="files/IMG_2971-1024x768.jpg" class="attachment-main" alt="" title="" style="border:#CCC 1px solid">							
                           
